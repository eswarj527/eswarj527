package TechM;

public class Odd_Numbers {
	
	    public static void main(String[] args) {
	        // Numbers from 1 to 99
	    	System.out.println("Odd Numbers from 1 to 99");
	        for (int i = 1; i <= 99; i+=2) {
	            System.out.println(i); // odd number
	        }
	    }
	}
